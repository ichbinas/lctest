package com.company;

class MyCombo {
    private Integer x1;
    private Integer x2;
    public MyCombo (Integer x1, Integer x2) {
        this.x1 = x1;
        this.x2 = x2;
    }

    public void setX1(Integer x1) {
        this.x1 = x1;
    }

    public void setX2(Integer x2) {
        this.x2 = x2;
    }

    public Integer getX1() {
        return x1;
    }

    public Integer getX2() {
        return x2;
    }
}